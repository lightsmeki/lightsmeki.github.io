
//显示与隐藏
$(document).ready(function(){
  $("#hide").click(function(){
  $("#userlogin").hide();
  });
  $("#show").click(function(){
  $("#userlogin").show();
  });
  
  $("#hideclass").click(function(){
  $("#classlist").hide();
  });
  $("#showclass").click(function(){
  $("#classlist").show();
  });
});